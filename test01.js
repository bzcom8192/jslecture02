console.log("Hello, World!");

// data types
// string
console.log("AAAA");
console.log('BBBB');
console.log(`CCCC`);

// number
console.log(123);       // integer
console.log(12.34);     // float

// boolean
console.log(true);      // 1
console.log(false);     // 0

// null
console.log(null);      // empty value

// object {} - key:value
console.log({
    name: "AAAA",
    age: 20,
    isStudent: true,
    address: {
        province: "Bangkok",
        country: "Thailand"
    }
});

// array [] - list of values
console.log([10, 20, 30, 40, 50]);

// undefined
let x;
console.log(x);
